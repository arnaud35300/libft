int	ft_atoi(const char *nptr)
{
	int r;
	int s;

	r = 0;
	s = 1;
	while (*nptr == 32 || (*nptr >= 8 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			s = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		r = r * 10 + *nptr - 48;
		nptr++;
	}
	return (r * s);
}
