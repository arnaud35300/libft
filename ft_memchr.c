#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	while (n && *(char *)s != '\0')
	{
		if (c == *(char *)s)
			return ((void *)s);
		s++;
		n--;
	}
	return (NULL);
}
